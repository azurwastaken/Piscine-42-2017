int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	while (i < nb && (nb % i != 0))
	{
		i++;
	}
	if (i == nb)
		return (1);
	else
		return (0);
}

int	ft_find_next_prime(int nb)
{
	if (ft_is_prime(nb) == 1)
		return (nb);
	else
		return (ft_find_next_prime(nb + 1));
}
