int	divide(int a, int b)
{
	return (a / b);
}

int	substract(int a, int b)
{
	return (a - b);
}

int	add(int a, int b)
{
	return (a + b);
}

int	multiple(int a, int b)
{
	return (a * b);
}

int	modulo(int a, int b)
{
	return (a % b);
}
