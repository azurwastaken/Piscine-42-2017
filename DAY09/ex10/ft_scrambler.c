void	ft_swap(int *a, int *b)
{
	int c;

	c = *a;
	*a = *b;
	*b = c;
}

void	ft_scrambler(int ***a, int *b, int *******c, int ****d)
{
	ft_swap(******c, **a);
	ft_swap(**a, ***d);
	ft_swap(**a, b);
}
