int		is_letter(char c)
{
	int i;

	i = 0;
	if (c >= 'a' && c <= 'z')
		i = 1;
	if (c >= 'A' && c <= 'Z')
		i = 2;
	return (i);
}

char	*ft_rot42(char *str)
{
	int		i;
	char	*s;

	s = str;
	i = 0;
	while (s[i] != '\0')
	{
		if (is_letter(s[i]) == 1)
			s[i] = (((s[i] - 'a') + 42) % 26) + 'a';
		if (is_letter(s[i]) == 2)
			s[i] = (((s[i] - 'A') + 42) % 26) + 'A';
		i++;
	}
	return (s);
}
