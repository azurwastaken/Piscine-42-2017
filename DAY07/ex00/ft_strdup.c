#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

char	*ft_strdup(char *src)
{
	char *mem_alloc;

	if (!src)
		return (0);
	else
	{
		mem_alloc = (char *)malloc(sizeof(*src) * (ft_strlen(src) + 1));
		mem_alloc = ft_strcpy(mem_alloc, src);
	}
	return (mem_alloc);
}
